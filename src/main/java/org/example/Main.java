package org.example;

public class Main {
    public static void main(String[] args) {

        System.out.println(addition(3,2));
        System.out.println(multiplication(5, 8));
    }

    public static int addition(int a, int b) {
        return a + b;
    }

    public static int multiplication(int a, int b) {
        return a * b;
    }
}