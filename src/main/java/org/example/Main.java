package org.example;

public class Main {
    public static void main(String[] args) {

        System.out.println(addition(3,2));
        System.out.println(division(10,5));
    }

    public static int addition(int a, int b) {

        return a + b;
    }

    public static int division(int a, int b) {
        return a / b;
    }
}